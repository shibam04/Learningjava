package ex07_incrementdecrement;

public class Prog6_decre
{
    public static void main(String[] args)
    {
       int x=10;
       int y= x + x--;
        System.out.println(y);
        System.out.println(x);
        int x1=10;
        int y1= x1 + --x1;
        System.out.println(y1);
        System.out.println(x1);



    }
}
